a = 10
b = 20
c = a + b  # Adding two integer variables
print(c)  # Output: 30
print(type(c))  # Output: <class 'int'>
d = "30.2"  # String variable
print(d)  # Output: 30.2
print(type(d))  # Output: <class 'str'>
e = float(d)  # Converting string to float
print(e)  # Output: 30.2
print(type(e))  # Output: <class 'float'>