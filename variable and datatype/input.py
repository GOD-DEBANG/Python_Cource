a = int(input("Enter an integer: ")) # Taking integer input from user
b = int(input("Enter another integer: ")) # Taking another integer input from user
c = a + b  # Adding two integer variables
print(c)  # Output: Sum of a and b

d = input("Enter a string: ")  # Taking string input from user
e = input("Enter a float: ")  # Taking float input from user
f = d+e  # Concatenating string and float (float will be converted to string)
print(f)  # Output: The string entered by the user